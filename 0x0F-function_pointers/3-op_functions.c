

/**
  * op_add - sum of two given integers.
  * @a: first term of the sum,
  * @b: second term.
  * Return: sum.
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - substraction of two integers.
  * @a: first term,
  * @b: second term,
  * Return: substraction result.
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - multiplies two given integers.
  * @a: first factor,
  * @b: second factor.
  * Return: product.
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - divides two integers.
  * @a: dividend,
  * @b: divisor.
  * Return: quotient.
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - compute remainder of the division of two integers.
  * @a: dividend,
  * @b: divisor.
  * Return: remainder.
  */
int op_mod(int a, int b)
{
	return (a % b);
}
